class DemoDataTypes
{
	public static void main(String args[])
	{
		int x=134;
		byte b=10;
		char  ch='$';
		double db=56.875;
		float ft=56.56F;
		boolean b1=true;

		System.out.println("x="+x);
		System.out.println("byte="+b);
		System.out.println("char="+ch);
  		System.out.println("double="+db);
		System.out.println("float="+ft);
		System.out.println("boolean="+b1);
	}
}